//app.js
App({
  globalData:{
    userInfo: {},
    hasUserInfo: false,
    canIUseGetUserProfile: false,
    indatabase:false,
    openid:'',
    session:'',
    avatarUrl:'',
    userInfo:'',
  },

  onLaunch: function () {
    if (!wx.cloud) {
      console.error('请使用 2.2.3 或以上的基础库以使用云能力')
    } else {
      wx.cloud.init({
        // env 参数说明：
        //   env 参数决定接下来小程序发起的云开发调用（wx.cloud.xxx）会默认请求到哪个云环境的资源
        //   此处请填入环境 ID, 环境 ID 可打开云控制台查看
        //   如不填则使用默认环境（第一个创建的环境）
        env: 'trialtrial-6g44kz5g58ae4df0',
        traceUser: true,
      })
    }
  },
  getUserProfile(e) {
    // 推荐使用wx.getUserProfile获取用户信息，开发者每次通过该接口获取用户个人信息均需用户确认
    // 开发者妥善保管用户快速填写的头像昵称，避免重复弹窗
    wx.getUserProfile({
      desc: '用于为您提供个性化方案', // 声明获取用户个人信息后的用途，后续会展示在弹窗中，请谨慎填写
      success: (res) => {
        this.userInfo= res.userInfo,
        this.hasUserInfo= true
        console.log(this.userInfo)
      }
    })
  },
})
